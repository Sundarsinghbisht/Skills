#include<iostream>
using namespace std;
int main()
{
    int n ;
    cout<<"Enter a number;"<<endl;
    cin>>n;
    switch (n)
    {
    case 100:
    cout<<"100"<<endl;
        break;
    
    case 200:
    cout<<"200"<<endl;
        break;
    
    case 300:
    cout<<"300"<<endl;
        break;
    
    case 400:
    cout<<"400"<<endl;
        break;
    
    default:
    cout<<"Enter a valid number"<<endl;
        break;
    }

    
} 
